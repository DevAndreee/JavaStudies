    // Attributes
    String brand;
    int speed;

    // Method without parameters
    void startEngine() {
        System.out.println(brand + " engine started!");
    }

    // Method with parameter
    void accelerate(int increment) {
        speed += increment;
        System.out.println(brand + " accelerated. Current speed: " + speed + " km/h");
    }

