public class PrimitivesTypes {
    public static void main (String[] args){
        
        // Int 
        byte numByte = 127;
        short numShort = 3000;
        int numInt = 10;
        long numLong = 1000000L;
        char character = 'A';
        char ascii = 65;
        char unicode = '\u0041';
        
        // Dec
        float numFloat = 1.0f;
        double numDouble = 1.000;
        
        // Bool
        boolean trueCase = true;
        boolean falseCase = false;
        

    }
}

/*
        byte → small integer from -128 to 127
        short → short integer from -32,768 to 32,767
        int → standard 32-bit integer
        long → long integer, use L at the end
        char → stores a Unicode character (e.g., 'A')
        ascii → char with numeric ASCII value
        unicode → char in Unicode format (e.g., '\u0041' = A)
        float → lightweight decimal number, needs 'f' suffix
        double → more precise decimal number, default for decimals
        boolean → represents true or false
        */